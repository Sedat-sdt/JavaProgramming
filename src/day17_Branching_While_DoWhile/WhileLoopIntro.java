package day17_Branching_While_DoWhile;

public class WhileLoopIntro {

    public static void main(String[] args) {

        if(true){
            System.out.println("Wooden Spoon");//gets executed 1 time
        }

        System.out.println("--------------------------------");

        while(true){
            System.out.println("Wooden Spoon");//gets executed infinite times now
        }


    }
}
