package day17_Branching_While_DoWhile;

public class LoopPractices {
    public static void main(String[] args) {

        for (int i = 1; i <=10 ; i++) {
            System.out.println(i);
        }
        System.out.println("--------------------------------------");
        int j=1;                //initialization
        while(j <= 10){         //condition
            System.out.println(j);
            j++;                //iteration
        }

    }
}
